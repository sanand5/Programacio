    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        
        double precio = 8;
        double iva;
        double pvp;
        //Opciones obligatorias
        //Comprueba que se ha elegido un tipo de hamburguesa
        if(jrbPollo.isSelected() || jrbCerdo.isSelected() || jrbTernera.isSelected() || jrbVegana.isSelected()){
            if(jrbTernera.isSelected() || jrbVegana.isSelected()){
                precio += 1;
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debe elegir un tipo de hamburguesa", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }
        //Comprueba que se ha elegido un tipo de pan
        if(!jrbNormal.isSelected() && !jrbIntegral.isSelected() && !jrbCenteno.isSelected()){
            JOptionPane.showMessageDialog(this, "Debe elegir un tipo de pan", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }
        //Comprueba que se ha elegido un tipo de patatas
        if(jrbFritas.isSelected() || jrbGajo.isSelected() || jrbCaseras.isSelected()){
            if(jrbCaseras.isSelected()){
                precio += 1;
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debe elegir un tipo de patatas", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }
        //Comprueba que se ha elegido una bebida
        if(!jrbCola.isSelected() && !jrbNaranja.isSelected() && !jrbLimon.isSelected() && !jrbAgua.isSelected() && !jrbCerveza.isSelected()){
            JOptionPane.showMessageDialog(this, "Debe elegir una bebida", "Atención", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        //Opciones extra/adicionales 
        if(cbxHamburguesaDoble.isSelected()){
            precio += 2;
        }
        if(cbxExtraQueso.isSelected()){
            precio += 0.5;
        }
        if(cbxExtraPatatas.isSelected()){
            precio += 1;
        }
        //Cuenta la cantidad de salsa seleccionada    
        int cantidadSalsa = (Integer)spnKetchup.getValue() + (Integer)spnBarbacoa.getValue() +
                            (Integer)spnMostaza.getValue() + (Integer)spnThai.getValue();
        precio += (double)cantidadSalsa * 0.5;        
        //Tipo de reparto
        if(!jrbDomicilio.isSelected() && !jrbRecogidaLocal.isSelected()){
           JOptionPane.showMessageDialog(this, "Debe elegir el tipo de reparto", "Atención", JOptionPane.WARNING_MESSAGE);
           return; 
        }else if(jrbRecogidaLocal.isSelected()){
            precio -= precio * 20 / 100;
        }      
        //Calcula los precios
        iva = precio * 21 / 100;
        pvp = precio + iva;
        ftxtPrecio.setValue(precio);
        ftxtIva.setValue(iva);
        ftxtPvp.setValue(pvp);
    }//GEN-LAST:event_btnCalcularActionPerformed

