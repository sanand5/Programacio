    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed

        String strA = txtNumA.getText();
        String strB = txtNumB.getText();

        long numA;
        long numB;
        long res;

        try {
            numA = Integer.parseInt(strA);
            numB = Integer.parseInt(strB);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Valor introducido no válido:\n" + e, "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // SUMATORIO
        if (rbSum.isSelected()) {
            res = 0;
            for (long i = numA; i <= numB; i++) {
                res += i;
            }
            lblRes.setText("" + res);

        // PRODUCTORIO
        } else if (rbProd.isSelected()) {
            res = 1;
            for (long i = numA; i <= numB; i++) {
                res *= i;
            }
            lblRes.setText("" + res);

        // POTENCIA
        } else if (rbExp.isSelected()) {
            res = (long) Math.pow(numA, numB);
            lblRes.setText("" + res);

        // NINGUNA OPCIÓN SELECCIONADA
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona una opción", "Aviso", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnCalcActionPerformed

